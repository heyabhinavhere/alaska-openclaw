def test_template_fill_expands_table_rows():
    d = _tmp()
    tpl = _make_template([
        {"type": "table", "columns": ["Account", "Balance"], "rows": [["{{a}}", "{{b}}"]]},
    ], d)
    out = str(d / "accounts.docx")
    res = A.render_docx_from_template(
        tpl, {}, out,
        table_fills=[{"locate": "Account", "rows": [["Visa", "$100"], ["Amex", "$200"]]}])
    assert res["table_fills"] == ["Account"]
    text = _docx_text(out)
    assert "Visa" in text and "Amex" in text and "$200" in text
    assert "{{" not in text
    assert A.validate_docx(out)["ok"] is True


def test_template_fill_table_missing_prototype_raises():
    d = _tmp()
    # A table with a header row but no data row to clone.
    tpl = _make_template([{"type": "table", "columns": ["Only", "Header"], "rows": []}], d)
    out = str(d / "x.docx")
    _raises(docx.TemplateRenderError, A.render_docx_from_template, tpl, {}, out,
            table_fills=[{"locate": "Only", "rows": [["1", "2"]]}])


