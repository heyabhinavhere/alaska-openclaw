def _tmp() -> Path:
    return Path(tempfile.mkdtemp(prefix="alaska_artifacts_test_"))


def _docx_text(path) -> str:
    xml = zipfile.ZipFile(path).read("word/document.xml")
    root = ET.fromstring(xml)
    return "\n".join(t.text or "" for t in root.iter(_W + "t"))


def _raises(exc_types, fn, *args, **kwargs):
    try:
        fn(*args, **kwargs)
    except exc_types as exc:
        return exc
    raise AssertionError("expected %s to be raised" % (exc_types,))


def _kitchen_sink_spec() -> dict:
    return {
        "meta": {"title": "Weekly Digest", "subtitle": "BON Credit", "author": "Alaska",
                 "date": "2026-06-05", "format": "letter"},
        "options": {"accent_color": "1F4E79", "font": "Calibri"},
        "blocks": [
            {"type": "heading", "level": 1, "text": "Summary"},
            {"type": "paragraph", "text": "Shipped 4 tasks this week."},
            {"type": "bullets", "items": ["First item", "Second item"]},
            {"type": "numbered", "items": ["Step one", "Step two"]},
            {"type": "table", "title": "Owners", "columns": ["Name", "Tasks"],
             "rows": [["Sandeep", "2"], ["Pankaj", "3"]], "align": ["left", "right"]},
            {"type": "callout", "style": "warning", "text": "One blocker is overdue."},
            {"type": "spacer"},
            {"type": "pagebreak"},
            {"type": "heading", "level": 2, "text": "Appendix"},
            {"type": "paragraph", "text": "Generated by the Alaska Artifact Service."},
        ],
    }


