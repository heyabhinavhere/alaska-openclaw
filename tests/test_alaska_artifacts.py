def test_validate_docx_catches_bad_zip():
    d = _tmp()
    p = d / "not.docx"
    p.write_text("this is not a zip", encoding="utf-8")
    res = A.validate_docx(str(p))
    assert res["ok"] is False and "bad_zip" in res["errors"]


def test_validate_docx_catches_missing_parts():
    d = _tmp()
    p = str(d / "incomplete.docx")
    with zipfile.ZipFile(p, "w") as zf:
        zf.writestr("[Content_Types].xml", "<Types/>")  # no word/document.xml
    res = A.validate_docx(p)
    assert res["ok"] is False
    assert any("missing_parts" in e for e in res["errors"])


def test_validate_docx_no_placeholders_lists_leftovers():
    d = _tmp()
