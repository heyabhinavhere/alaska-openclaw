class _FakeSlack:
    def __init__(self, responses):
        self.responses = responses
        self.calls = []
        self._i = 0

    def __call__(self, method, url, *, headers=None, body=None, timeout=None):
        self.calls.append({"method": method, "url": url, "headers": headers, "body": body})
        resp = self.responses[self._i]
        self._i += 1
        return resp


def _a_file() -> str:
    d = _tmp()
    src = str(d / "report.docx")
    A.render_docx_from_docflow(_kitchen_sink_spec(), src)
    return src


def test_slack_upload_success_runs_three_step_flow():
    fake = _FakeSlack([
        (200, json.dumps({"ok": True, "upload_url": "https://upload.example/abc",
                          "file_id": "F123"}).encode()),
        (200, b""),  # PUT bytes
        (200, json.dumps({"ok": True}).encode()),  # complete
    ])
    res = A.upload_artifact_to_slack(_a_file(), "C42", token="xoxb-test", http_request=fake)
    assert res["ok"] is True and res["file_id"] == "F123"
    assert len(fake.calls) == 3
    assert fake.calls[0]["url"] == slack_upload.GET_UPLOAD_URL
    assert fake.calls[1]["url"] == "https://upload.example/abc"
    assert fake.calls[2]["url"] == slack_upload.COMPLETE_UPLOAD_URL


def test_slack_upload_failure_preserves_file():
    path = _a_file()
    fake = _FakeSlack([(200, json.dumps({"ok": False, "error": "channel_not_found"}).encode())])
    res = A.upload_artifact_to_slack(path, "C42", token="xoxb-test", http_request=fake)
    assert res["ok"] is False and res["step"] == "get_upload_url"
    assert os.path.exists(path)  # artifact is never deleted on failure


def test_slack_upload_missing_file_is_reported_not_raised():
    fake = _FakeSlack([])
    res = A.upload_artifact_to_slack("/no/such/file.docx", "C42", token="xoxb-test", http_request=fake)
    assert res["ok"] is False and res["step"] == "read"


