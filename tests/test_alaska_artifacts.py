def test_store_artifact_writes_file_and_metadata():
    d = _tmp()
    base = str(d / "store")
    src = str(d / "report.docx")
    A.render_docx_from_docflow(_kitchen_sink_spec(), src)

    meta = A.store_artifact(src, "docx", "weekly-digest", "2026-06-05", base_dir=base)
    assert meta["artifact_id"] == "weekly-digest/2026-06-05/report.docx"
    assert os.path.exists(meta["path"])
    assert os.path.exists(meta["path"] + ".meta.json")
    assert os.path.exists(src)  # source preserved (copy, not move)
    assert meta["bytes"] > 0 and len(meta["sha256"]) == 64
    assert meta["created_at"]

    # round-trip via get_artifact_metadata
    loaded = A.get_artifact_metadata(meta["artifact_id"], base_dir=base)
    assert loaded == meta
    # index has the row
    rows = A.list_artifacts("weekly-digest", base_dir=base)
    assert len(rows) == 1 and rows[0]["artifact_id"] == meta["artifact_id"]


def test_store_artifact_no_overwrite_unless_requested():
    d = _tmp()
    base = str(d / "store")
    src = str(d / "report.docx")
    A.render_docx_from_docflow(_kitchen_sink_spec(), src)
    A.store_artifact(src, "docx", "audit", "1414", base_dir=base)
    # Second store of the same owner/run/name must refuse by default.
    _raises(store.ArtifactExistsError, A.store_artifact, src, "docx", "audit", "1414", base_dir=base)
    # ...unless overwrite is explicit.
    meta = A.store_artifact(src, "docx", "audit", "1414", base_dir=base, overwrite=True)
    assert meta["artifact_id"] == "audit/1414/report.docx"


def test_store_artifact_sanitizes_path_segments():
    d = _tmp()
    base = str(d / "store")
    src = str(d / "r.docx")
    A.render_docx_from_docflow(_kitchen_sink_spec(), src)
    meta = A.store_artifact(src, "docx", "../etc", "a/b", base_dir=base)
    # No path traversal escapes the base dir.
    assert os.path.normpath(meta["path"]).startswith(os.path.normpath(base) + os.sep)


def test_artifacts_base_env_override():
    d = _tmp()
    prev = os.environ.get("ALASKA_ARTIFACTS_DIR")
    os.environ["ALASKA_ARTIFACTS_DIR"] = str(d / "viaenv")
    try:
        assert store.artifacts_base() == str(d / "viaenv")
    finally:
        if prev is None:
            os.environ.pop("ALASKA_ARTIFACTS_DIR", None)
        else:
            os.environ["ALASKA_ARTIFACTS_DIR"] = prev


