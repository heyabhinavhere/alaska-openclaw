def test_render_docx_from_docflow_is_valid():
    out = _tmp() / "digest.docx"
    res = A.render_docx_from_docflow(_kitchen_sink_spec(), str(out))
    assert out.exists() and res["bytes"] > 0
    assert "word/document.xml" in res["parts"]
    # The delivery gate passes: valid zip, required parts, well-formed XML, no placeholders.
    gate = A.validate_docx(str(out))
    assert gate["ok"] is True, gate["errors"]
    text = _docx_text(out)
    assert "Summary" in text and "Sandeep" in text and "WARNING" in text


def test_render_docx_from_docflow_rejects_invalid_spec():
    out = _tmp() / "bad.docx"
    err = _raises(docx.DocflowValidationError, A.render_docx_from_docflow,
                  {"meta": {}, "blocks": []}, str(out))
    assert err.errors
    assert not out.exists()  # nothing written on invalid spec


