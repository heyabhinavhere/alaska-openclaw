def _make_template(blocks, dirpath) -> str:
    spec = {"meta": {"title": "Internal Report"}, "blocks": blocks}
    tpl = str(Path(dirpath) / "template.docx")
    A.render_docx_from_docflow(spec, tpl)
    return tpl


def test_template_fill_replaces_scalar_tokens():
    d = _tmp()
    tpl = _make_template([
        {"type": "paragraph", "text": "User: {{user_id}}"},
        {"type": "paragraph", "text": "Name: {{first_name}}"},
    ], d)
    out = str(d / "filled.docx")
    res = A.render_docx_from_template(
        tpl, {"{{user_id}}": "1414", "{{first_name}}": "Sandeep"}, out)
    assert res["replaced_keys"] == ["{{first_name}}", "{{user_id}}"]
    text = _docx_text(out)
    assert "1414" in text and "Sandeep" in text
    assert "{{" not in text
    assert A.validate_docx(out)["ok"] is True


def test_template_fill_unfilled_placeholder_fails_loudly():
    d = _tmp()
    tpl = _make_template([{"type": "paragraph", "text": "Owed: {{balance}}"}], d)
    out = str(d / "filled.docx")
    # We provide nothing for {{balance}} -> the post-fill guard must raise.
    err = _raises(docx.TemplateRenderError, A.render_docx_from_template, tpl, {}, out)
    assert "placeholder" in str(err).lower()


def test_template_fill_unknown_bracket_behavior_is_defined():
    d = _tmp()
    tpl = _make_template([{"type": "paragraph", "text": "See Appendix A for detail."}], d)
    # Inject a literal bracket token by filling a known token into a bracket form.
    tpl2 = _make_template([{"type": "paragraph", "text": "Ref {{ref}}"}], d)
    out = str(d / "with_bracket.docx")
    # Default: a leftover [bracket] placeholder is rejected.
    A.render_docx_from_template(tpl2, {"{{ref}}": "[Appendix A]"}, out,
                                forbid_placeholder_patterns=())  # we WANT the bracket in
    assert "[Appendix A]" in _docx_text(out)
    # And with the default guard on, the same content fails the validate gate.
    leftovers = A.validate_docx_no_placeholders(out)
    assert "[Appendix A]" in leftovers


def test_template_fill_forbids_substrings():
    d = _tmp()
    tpl = _make_template([{"type": "paragraph", "text": "Note {{n}}"}], d)
    out = str(d / "emdash.docx")
    err = _raises(docx.TemplateRenderError, A.render_docx_from_template,
                  tpl, {"{{n}}": "a—b"}, out, forbid_substrings=("—",))
    assert "forbidden" in str(err).lower()


