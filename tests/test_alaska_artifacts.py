def test_validate_docflow_accepts_kitchen_sink():
    assert docflow.validate_docflow(_kitchen_sink_spec()) == []


def test_validate_docflow_reports_problems():
    assert "meta.title is required" in docflow.validate_docflow({"meta": {}, "blocks": []})
    bad_block = {"meta": {"title": "T"}, "blocks": [{"type": "nope"}]}
    assert any("not in" in e for e in docflow.validate_docflow(bad_block))
    bad_table = {"meta": {"title": "T"}, "blocks": [
        {"type": "table", "columns": ["A", "B"], "rows": [["only-one"]]}]}
    assert any("expected 2" in e for e in docflow.validate_docflow(bad_table))


